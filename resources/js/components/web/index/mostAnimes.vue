<script>
import {error} from "@/other/techOperation.ts";

export default {
    data() {
        return {
            animeLists: [],
            error,
        };
    },
    mounted() {
        setTimeout(() => {
            this.fetchAllData();
        }, "1000");
    },
    methods: {
        async fetchAllData() {
            try {
                const response = await fetch("https://shikimori.one/api/graphql", {
                    method: "POST",
                    headers: {
                        "Content-Type": "application/json",
                        Accept: "application/json",
                    },
                    body: JSON.stringify({
                        query: `
              query {
                ongoingAnimes: animes(limit: 6, order: popularity, status: "ongoing", kind: "tv") {
                  id
                  name
                  russian
                  kind
                  score
                  poster {
                    main2xUrl
                  }
                  airedOn {
                    year
                  }
                }
                anonseAnimes: animes(season: "2024", limit: 6, order: name, status: "anons", kind: "tv") {
                  id
                  name
                  russian
                  kind
                  score
                  poster {
                    main2xUrl
                  }
                  airedOn {
                    year
                  }
                }
                topAnimes: animes(limit: 6, order: ranked, status: "released", kind: "tv") {
                  id
                  name
                  russian
                  kind
                  score
                  poster {
                    main2xUrl
                  }
                  airedOn {
                    year
                  }
                }
                releasedAnimes: animes(limit: 6, order: popularity, status: "released", kind: "tv") {
                  id
                  name
                  russian
                  kind
                  score
                  poster {
                    main2xUrl
                  }
                  airedOn {
                    year
                  }
                }
              }
            `,
                    }),
                });

                const data = await response.json();
                if (!response.ok) {
                    this.error();
                }
                this.animeLists.push({
                    title: "Онгоинги",
                    description: "Вступай в новые эпизоды приключений, следи за сюжетом!",
                    animes: data.data.ongoingAnimes,
                });

                this.animeLists.push({
                    title: "Анонсы",
                    description: "Узнавай первым о предстоящих релизах, которые ожидают нас!",
                    animes: data.data.anonseAnimes,
                });

                this.animeLists.push({
                    title: "Топ аниме",
                    description: "Погружайся в лучшие произведения аниме, отмеченные высшим призом!",
                    animes: data.data.topAnimes,
                });

                this.animeLists.push({
                    title: "Завершенные",
                    description: "Проведи время в компании классических аниме, наполненных волнением!",
                    animes: data.data.releasedAnimes,
                });
            } catch (error) {
                this.error()
            }
        },
        goToAnime(animeId) {
            this.$router.push(`/anime?animeId=${animeId}`);
        },
    },
};
</script>

